=begin
  Methods also know as functions are rather easy to create.
  We first start of with the "def" keyword which tells the
  compiler we are def-ining a new method followed by the
  methods name and brakets which and be avoided if the method
  calls no arguments. Finally we close our method with the end
  keyword

  eg. A simple functions layout

  def "method_name"() <- *New method definition*
    "Your code here"
  end <- *Conclude definition of method*

=end

def sayhi()
  puts "Hi, it's nice to meet you"
end
